	});
	
	var current = 0,
			$preview = $('#preview'),
			$carouselEl = $('#carousel'),
			$carouselItems = $carouselEl.children();
	var carousel = $carouselEl.elastislide({
		current: current,
		minItems: 4,
		// onClick: function(el, pos, evt) {
			// evt.preventDefault();
